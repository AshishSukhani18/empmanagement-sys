class Employee
{
	private int Empid;
	private String Name;
	private double salary;
	
	public int getEmpid()
	{
		return Empid;
	}
	public void setEmpid(int Empid)
	{
		this.Empid=Empid;
	}
	
	
	public String getName()
	{
		return Name;
	}
	public void setName(String Name)
	{
		this.Name=Name;
	}
	
	
	public double getSalary()
	{
		return salary;
	}
	public void setSalary(double salary)
	{
		this.salary=salary;
	}
	
}

